package com.example.TicTacToe.model;

public enum GameStatus {

    /**
     * The game is in progress.
     */
    IN_PROGRESS,

    /**
     * The first player won.
     */
    WON_PLAYER1,

    /**
     * The second player won.
     */
    WON_PLAYER2,

    /**
     * The game ended in a draw.
     */
    DRAW
}
